struct HopcroftKarp {
  static const int inf = 1e9;
  int n;
  vector<int> l, r, d;
  vector<vector<int>> g;
  HopcroftKarp(int _n, int _m) {
    n = _n;
    int p = _n + _m + 1;
    g.resize(p);
    l.resize(p, 0);
    r.resize(p, 0);
    d.resize(p, 0);
  }
  void add_edge(int u, int v) {
    g[u].push_back(v + n); //right id is increased by n, so is l[u]
  }
  bool bfs() {
    queue<int> q;
    for (int u = 1; u <= n; u++) {
      if (!l[u]) d[u] = 0, q.push(u);
      else d[u] = inf;
    }
    d[0] = inf;
    while (!q.empty()) {
      int u = q.front();
      q.pop();
      for (auto v : g[u]) {
        if (d[r[v]] == inf) {
          d[r[v]] = d[u] + 1;
          q.push(r[v]);
        }
      }
    }
    return d[0] != inf;
  }
  bool dfs(int u) {
    if (!u) return true;
    for (auto v : g[u]) {
      if(d[r[v]] == d[u] + 1 && dfs(r[v])) {
        l[u] = v;
        r[v] = u;
        return true;
      }
    }
    d[u] = inf;
    return false;
  }
  int maximum_matching() {
    int ans = 0;
    while (bfs()) {
      for(int u = 1; u <= n; u++) if (!l[u] && dfs(u)) ans++;
    }
    return ans;
  }
};

int dx[] = {2,2,-2,-2,1,-1,1,-1};
int dy[] = {1,-1,1,-1,2,2,-2,-2};

void solve(int test_case) {
    int n,k; cin>>n>>k;
    vii code(n+1,vi(n+1));
    vii vis(n+1,vi(n+1));
    int r = 1, c = 1;
    HopcroftKarp st((n*n+1)/2,(n*n+1)/2);
    for(int i = 1 ; i <= n ; i++){
        for(int j = 1 ; j <= n ; j++){
            if((i+j)&1) code[i][j] = r++;
            else code[i][j] = c++;
        }
    }

    for(int i = 0 ;i < k ;i++){
        int x,y; cin>>x>>y;
        vis[x][y] = 1;
    }

    for(int i = 1 ; i <= n ; i++){
        for(int j = 1 ; j <= n ; j++){
            if( ((i+j)&1) || !vis[i][j]) continue;
            for(int d =0  ; d < 8 ; d++){
                int x = i + dx[d];
                int y = j + dy[d];
                if(x > 0 && y > 0 && x <= n && y <= n && vis[x][y]){
                    st.add_edge(code[i][j],code[x][y]);
                }
            }
        }
    }

    cout<<st.maximum_matching()<<ln;
}

