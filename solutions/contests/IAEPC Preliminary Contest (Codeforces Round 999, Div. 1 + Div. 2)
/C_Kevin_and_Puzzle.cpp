int mod = 998244353;
const int N = 200005;
int a[N];
int dp[N][2];
int n;
int rec(int i,int ty){
    if(i == n-1){
        if(ty) return (a[i] >= 0 && a[i] <= (i+1)/2);
        return 1;
    }
    int &ret = dp[i][ty];
    if(~ret) return ret;
    if(ty == 0){
        int prv = (i ? a[i-1] : 0);
        int nxt = a[i+1];
        if(nxt - prv == 1) return rec(i+1,1);
        return ret = 0;
    }
    if(a[i] >= 0 && a[i] <= (i+1)/2){
        int res = rec(i+1,0)%mod;
        if(a[i] == a[i+1]) (res += rec(i+1,1))%=mod;
        return ret = res%mod;
    } 
    return ret = 0;
}
