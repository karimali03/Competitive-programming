int gcd(int a, int b) {
    if (!a || !b)
        return a | b;
    unsigned shift = __builtin_ctz(a | b);
    a >>= __builtin_ctz(a);
    do {
        b >>= __builtin_ctz(b);
        if (a > b)
            swap(a, b);
        b -= a;
    } while (b);
    return a << shift;
}


void solve(int test_case) {
    int n; cin >> n;
    vi v(n); cin >> v;
    int mx = *max_element(all(v));
    int gc = 0;
    for (int num : v) gc = gcd(gc, num);
    int cnt = count(all(v), gc);
    if(cnt) return void(cout<<n-cnt<<ln);
    if(cnt == 0) cnt = n - 1;
    else cnt = n - cnt;

    sort(v.begin(), v.end());
    v.erase(unique(v.begin(), v.end()), v.end());

    vi dp(mx + 1, INT_MAX);
    for (int num : v) dp[num] = 0;

    for (int num : v) {
        for (int j = mx; j >= 1; --j) {
            if (dp[j] != INT_MAX) {
                int new_gcd = gcd(j, num);
                dp[new_gcd] = min(dp[new_gcd], dp[j] + 1);
            }
        }
    }

    cout << cnt + dp[gc] << ln;
}

