/*
  x , y 
  x = y  res  = 0
  x < y   res = x%y + y%x = x + ( y - m * x)  m >= 1 
  res = x + y - x = y  <= y
   f(a,b) <= b  
   f(b,mx) <= mx ->  b + mx%b >= b
   max involve in res
   x < y < x*2 ->   res = x + y%x = y
   else take y as max but loop ove prev elements (will happen log(1e9) times)
*/

int calc(int x,int y){
    return x%y+y%x;
}
void solve(int test_case) {
    int n; cin>>n;
    vi v(n); cin>>v;
    int mx = 0;
    int res = 0;
    for(int i = 0 ; i <  n ; i++){
        if(v[i] <= mx){
            res = max(res , calc(mx,v[i]));
        }
        else if(v[i] > mx && v[i] < mx*2){
            res = max(res , v[i]);
            mx = v[i];
        }
        else{
            for(int j = 0 ; j < i ; j++){
                res = max(res , calc(v[i],v[j]));
            }
            mx = v[i];
        }
        cout<<res<<" ";
    }
    cout<<ln;
}

